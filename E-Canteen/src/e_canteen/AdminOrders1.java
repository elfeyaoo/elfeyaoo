    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
    DefaultTableModel model1 = (DefaultTableModel) jTable3.getModel();
        DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();

        // Get selected row index
        int selectedRowIndex = jTable3.getSelectedRow();

        if (selectedRowIndex != -1) { // If a row is selected
            // Get values from the selected row
            String item = (String) model1.getValueAt(selectedRowIndex, 0);
            String quantity = (String) model1.getValueAt(selectedRowIndex, 1);

            // Add the selected row to jTable2
            model2.addRow(new Object[]{item, quantity});

            // Remove the selected row from jTable3
            model1.removeRow(selectedRowIndex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

