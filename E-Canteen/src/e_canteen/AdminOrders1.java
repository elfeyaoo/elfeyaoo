    private void fetchCanteenData() {
    try {
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ecanteen?useSSL=false", "root", "@zgardi@n#1234");
        Statement statement = connection.createStatement();
        ResultSet resultSet = statement.executeQuery("SELECT * FROM canteen2");

        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.setRowCount(0); // Clear existing rows

        while (resultSet.next()) {
            String username = resultSet.getString("username");
            String item = resultSet.getString("item");
            String quantity = resultSet.getString("quantity");
            model.addRow(new Object[]{username, item, quantity});
        }

        resultSet.close();
        statement.close();
        connection.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
}
